#!/usr/bin/env node
/**
 * build-icon-sprite.mjs — V5-M4 (SIK-76) — Compile per-icon SVG sources
 * into a single sprite consumed via `<svg><use href="/icons.svg#name" />`.
 *
 * Why a build step instead of authoring the sprite directly:
 *   - One canonical source per icon (`packages/design-system/src/icons/<name>.svg`)
 *     keeps `lint-icon-style.mjs` (V5-M2 task 6) honest — the lint scans
 *     individual files and verifies viewBox / stroke-width / linecap.
 *   - The sprite is regenerated deterministically; no drift between source
 *     SVG and sprite symbol contents.
 *   - Each icon batch lands as its own commit (tasks 19.1-19.6b) by simply
 *     dropping new files into the source directory and re-running this
 *     script.
 *
 * Output:
 *   `apps/web/public/icons.svg` — the public sprite, served from
 *   `/icons.svg` and used as `<use href="/icons.svg#chevron-left" />`.
 *   This file used to host the V4 social/footer sprite which is no longer
 *   referenced by any apps/web/src/** code (verified before V5-M4 wave 0);
 *   wave 0 swaps the content to the V5 sprite skeleton.
 *
 * Symbol id rules (per design.md §C.5.6):
 *   - Symbol id = file stem in kebab-case (e.g. `chevron-left.svg`
 *     → `<symbol id="chevron-left" ...>`).
 *   - Outline icons preserve the source's stroke / stroke-width / linecap
 *     attributes so currentColor + theme switching work.
 *   - Filled variants (*-filled.svg) preserve fill="currentColor".
 *
 * Determinism:
 *   - Sources are read in lexicographic order so the sprite output is
 *     stable across runs.
 *   - Output is pretty-printed with 2-space indent so diffs on incremental
 *     adds stay reviewable.
 *
 * Usage:
 *   `node packages/design-system/scripts/build-icon-sprite.mjs`
 *   or via the `npm run build:icons -w @sikao/design-system` script.
 */

import { readdirSync, readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs';
import { join, dirname, relative, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const SCRIPT_DIR = fileURLToPath(new URL('.', import.meta.url));
const REPO_ROOT = join(SCRIPT_DIR, '..', '..', '..');
const SOURCE_DIR = join(REPO_ROOT, 'packages', 'design-system', 'src', 'icons');
const OUTPUT_PATH = join(REPO_ROOT, 'apps', 'web', 'public', 'icons.svg');

const HEADER = `<?xml version="1.0" encoding="UTF-8"?>
<!--
  V5 icon sprite — generated from packages/design-system/src/icons/*.svg
  by packages/design-system/scripts/build-icon-sprite.mjs.

  DO NOT edit this file directly. Add a source SVG to the directory above
  and re-run \`npm run build:icons -w @sikao/design-system\`.
-->
<svg xmlns="http://www.w3.org/2000/svg" style="display:none" aria-hidden="true">`;
const FOOTER = `</svg>
`;

function listSvgs(dir) {
  if (!existsSync(dir)) return [];
  return readdirSync(dir)
    .filter((name) => name.endsWith('.svg'))
    .sort();
}

function deriveSymbolId(filename) {
  return basename(filename, '.svg');
}

/**
 * Convert a single source `<svg ...>...</svg>` document into a
 * `<symbol id="<id>" ...>...</symbol>` fragment by:
 *   - replacing the outer `<svg>` tag with `<symbol id="...">`
 *   - preserving stroke / stroke-width / fill / linecap / linejoin
 *     attributes for both stroke-only and filled variants
 *   - dropping xmlns (the parent <svg> already declares it)
 */
function svgToSymbol(source, id) {
  const trimmed = source.replace(/<\?xml[^?]*\?>\s*/g, '').trim();
  const openMatch = trimmed.match(/^<svg\b([^>]*)>/i);
  if (!openMatch) {
    throw new Error(`build-icon-sprite: source does not start with <svg> (id="${id}")`);
  }
  const closeIdx = trimmed.lastIndexOf('</svg>');
  if (closeIdx === -1) {
    throw new Error(`build-icon-sprite: source missing </svg> (id="${id}")`);
  }
  const attrs = openMatch[1];
  // Strip xmlns; the sprite parent already declares it.
  const cleanAttrs = attrs.replace(/\s+xmlns\s*=\s*["'][^"']*["']/g, '');
  const inner = trimmed.slice(openMatch[0].length, closeIdx).trim();
  return `  <symbol id="${id}"${cleanAttrs}>\n${indent(inner, 4)}\n  </symbol>`;
}

function indent(text, spaces) {
  const pad = ' '.repeat(spaces);
  return text
    .split('\n')
    .map((line) => (line.length === 0 ? line : pad + line))
    .join('\n');
}

function main() {
  const sources = listSvgs(SOURCE_DIR);
  const symbols = [];
  for (const filename of sources) {
    const fullPath = join(SOURCE_DIR, filename);
    const content = readFileSync(fullPath, 'utf8');
    const id = deriveSymbolId(filename);
    symbols.push(svgToSymbol(content, id));
  }

  const sprite = `${HEADER}\n${symbols.join('\n')}\n${FOOTER}`;

  // Ensure output directory exists; vite copies apps/web/public/* verbatim.
  const outDir = dirname(OUTPUT_PATH);
  if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true });
  writeFileSync(OUTPUT_PATH, sprite, 'utf8');

  const relOut = relative(REPO_ROOT, OUTPUT_PATH).replace(/\\/g, '/');
  console.log(`✓ icon sprite built: ${relOut} (${sources.length} symbols)`);
  if (sources.length > 0) {
    console.log(`  symbols: ${sources.map((s) => deriveSymbolId(s)).join(' / ')}`);
  }
}

main();
